// 인수를 생략하면 10진수 문자열을 반환한다.
(10).toString(); // -> "10"
// 2진수 문자열을 반환한다. 숫자 16을 2진법으로 반환
(16).toString(2); // -> "10000"
// 8진수 문자열을 반환한다. 숫자 16을 8진법으로 반환
(16).toString(8); // -> "20"
// 16진수 문자열을 반환한다.
(16).toString(16); // -> "10"
//숫자 100을 2진법으로 반환
console.log((100).toString(2)); // -> "1100100"